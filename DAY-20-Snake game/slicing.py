piano_keys = ['a','b','c','d','e','f','g']

print(piano_keys[2:5])
# alternate item
print(piano_keys[2:5:2])
# get every second item
print(piano_keys[::2])
# inverse the list
print(piano_keys[::-1])
# rest of list from specified position
print(piano_keys[2:])
# all items upto specified posn..
print(piano_keys[:5])

